import sys
from collections import *
from itertools import chain

# 读取网格的行数 h 和列数 w
# 使用海象运算符 := 将 sys.stdin 赋值给 f，后续继续用 f 迭代剩余行
h, w = map(int, next(f := sys.stdin).split())

a = []  # 二维网格，存储为列表的列表
d = defaultdict(list)  # 传送门字典：字符 -> 该字符出现的所有坐标列表

for i, s in enumerate(f):
    # 将每行字符展开为列表，并在末尾追加 '#' 作为右侧边界（墙）
    # 这样访问 a[i][w] 时永远是 '#'，无需单独做列越界判断
    a.append([*s] + ["#"])
    for j, x in enumerate(s):
        # ASCII 大小：'#'=35  '.'=46  数字48-57  字母65+
        # x > '.' 意味着 x 是字母或数字，即该格为传送门
        if x > ".":
            d[x].append((i, j))  # 记录传送门坐标

# 在网格底部追加一行全为 '#' 的边界行
# 这样访问 a[h][j] 或 a[-1][j] 时永远是 '#'，无需单独做行越界判断
a.append("#" * w)

dist = -1  # 最终答案：最短步数，-1 表示不可达

# BFS 队列，每个元素为 (当前坐标, 当前格字符, 已走步数)
# 起点为 (0, 0)，字符为 a[0][0]，步数为 0
q = deque([((0, 0), a[0][0], 0)])
a[0][0] = "#"  # 将起点标记为已访问（'#' 既作为墙也作为访问标记）

t = h - 1, w - 1  # 终点坐标（右下角）

while q:
    # 从队列右端取出节点
    # 配合下方的 appendleft，整体构成反向 FIFO 队列，等价于标准 BFS
    u, c, r = q.pop()

    if u == t:  # 到达终点，记录最短步数并退出
        dist = r
        break

    i, j = u

    # 将两类"邻居"合并遍历：
    #   1. d.pop(c, [])：与当前格字符相同的所有传送门坐标（传送门跳跃）
    #      使用 pop 同时将其从字典中删除，确保不会被重复加入队列
    #   2. 上下左右四个相邻格（常规移动）
    # 两类移动均视为 1 步
    for v in chain(d.pop(c, []), [(i - 1, j), (i + 1, j), (i, j - 1), (i, j + 1)]):
        i, j = v
        c = a[i][j]
        # c > '#' 表示该格可通行且未访问
        # 可通行字符：'.'(普通格) 及字母/数字(传送门格)，均满足 > '#'(=35)
        if c > "#":
            q.appendleft((v, c, r + 1))  # 加入队列左端，步数 +1
            a[i][j] = "#"  # 标记为已访问，防止重复入队

print(dist)
